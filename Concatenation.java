// Program in Java to use + operator for concatenation.
class Concatenation{
  public static void main(String args[]){
    // Creating two strings
    String str1 = "Hello";
    String str2 = "EduWe";
    // Concatenating the two strings using + operator
    String str3 = str1 + str2;
    System.out.println("The new String is : "+str3);
    // Creating a integer variable
    int num = 10;
    // Creating a string
    String str4 = "Number: ";
    // Concatenating the string and integer using + operator
    String str5 = str4 + num;
    System.out.println(str5);
  }
}

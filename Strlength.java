// Program in Java to compute length of a string.
class Strlength{
  public static void main(String args[]){
    String str = "Welcome to EduWe.";
    int len1, len2;
    len1 = str.length();
    len2 = "EduWe".length();
    System.out.println("Number of characters in the str: "+len1);
    System.out.println("Number of characters in the string literal: "+len2);
  }
}
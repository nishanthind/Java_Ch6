// Program in Java to perform unboxing.
class Unbox{
  public static void main(String args[]){
    // Unboxing of Integer to int
    Integer i = 234; // Integer object
    int j = i; // Assigning Integer object to int
    System.out.println("value of Integer converted to int: "+j);
    // Unboxing of Double to double
    Double d = 45.23456; // Double object
    double e = d; // Assigning Double object to double 
    System.out.println("value of Double is converted to double: "+e);
  }
}